package com.aohuan.bean;

public class UserBean extends CodeBean{
	
	public User user;
	
	public User getUser() {
		return user;
	}
	public void setUser(User user) {
		this.user = user;
	}
	
	public class User{
		String id;
		String username;
		String image;
		
		public String getId() {
			return id;
		}
		public void setId(String id) {
			this.id = id;
		}
		public String getUsername() {
			return username;
		}
		public void setUsername(String username) {
			this.username = username;
		}
		public String getImage() {
			return image;
		}
		public void setImage(String image) {
			this.image = image;
		}
		@Override
		public String toString() {
			return "User [id=" + id + ", username=" + username + ", image="
					+ image + "]";
		}
		
	}

	@Override
	public String toString() {
		return "UserBean [user=" + user + ", code=" + code + ", message="
				+ message + "]";
	}

	
}
